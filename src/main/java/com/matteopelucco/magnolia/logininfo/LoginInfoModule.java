package com.matteopelucco.magnolia.logininfo;

public class LoginInfoModule {

	private String loginSummary;
	private String loginDetail;
	
	public String getLoginSummary() {
		return loginSummary;
	}

	public void setLoginSummary(String loginSummary) {
		this.loginSummary = loginSummary;
	}

	public String getLoginDetail() {
		return loginDetail;
	}

	public void setLoginDetail(String loginDetail) {
		this.loginDetail = loginDetail;
	}

}
